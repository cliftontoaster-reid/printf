/// @brief Prints an unsigned integer to the standard output.
///
/// This function writes the unsigned integer 'nb' to the standard output.
/// It returns the number of characters written (excluding the null byte).
///
/// @param nb The unsigned integer to print.
/// @return The number of characters written.
int	ft_putunbr(unsigned int nb);

/// @brief Prints an integer to the standard output.
///
/// This function writes the integer 'nb' to the standard output.
/// It returns the number of characters written (excluding the null byte).
///
/// @param nb The integer to print.
/// @return The number of characters written.
int	ft_putnbr(int nb);
